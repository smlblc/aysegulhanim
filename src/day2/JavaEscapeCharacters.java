package day2;

public class JavaEscapeCharacters {
    public static void main(String []args){
        // new line -> \n
        System.out.println("Techno\nStudy");
        //quote-> \"
        System.out.println("\"Hi\"");
        // tabulation-. \t
        System.out.println("\t hello");
        // backslash-> \\     //-> this is slash
        System.out.println("\\");


    }

}
